package of.shop.model;



public class CartResponse {

	private String proIdString;
	private String proPhotoString;
	private String proNameString;
	private String proDescString;
	private String proPriceString;
	private String proItemString;
	private String proNumString;
	private String proShippString;
	public String getProIdString() {
		return proIdString;
	}
	public void setProIdString(String proIdString) {
		this.proIdString = proIdString;
	}
	public String getProPhotoString() {
		return proPhotoString;
	}
	public void setProPhotoString(String proPhotoString) {
		this.proPhotoString = proPhotoString;
	}
	public String getProNameString() {
		return proNameString;
	}
	public void setProNameString(String proNameString) {
		this.proNameString = proNameString;
	}
	public String getProDescString() {
		return proDescString;
	}
	public void setProDescString(String proDescString) {
		this.proDescString = proDescString;
	}
	public String getProPriceString() {
		return proPriceString;
	}
	public void setProPriceString(String proPriceString) {
		this.proPriceString = proPriceString;
	}
	public String getProItemString() {
		return proItemString;
	}
	public void setProItemString(String proItemString) {
		this.proItemString = proItemString;
	}
	public String getProNumString() {
		return proNumString;
	}
	public void setProNumString(String proNumString) {
		this.proNumString = proNumString;
	}
	public String getProShippString() {
		return proShippString;
	}
	public void setProShippString(String proShippString) {
		this.proShippString = proShippString;
	}
}
